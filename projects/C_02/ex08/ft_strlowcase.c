char	*ft_strlowcase(char *str)
{
	char	*ch;

	ch = str;
	while (*ch != '\0')
	{
		if (*ch >= 'A' && *ch <= 'Z')
			*ch = *ch - 'A' + 'a';
		ch++;
	}
	return (str);
}
